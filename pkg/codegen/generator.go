func (g *Generator) generateMapValue(value *cucarachareflex.Value, indent int, outputEnd string) error {
	// All maps are now consistently map[*Value]*Value
	mapValues, ok := value.Value.(map[*cucarachareflex.Value]*cucarachareflex.Value)
	if !ok {
		return fmt.Errorf("expected map value to be map[*Value]*Value, got %T", value.Value)
	}

	// Check if this is actually a struct (Kind=struct but stored as map[*Value]*Value internally)
	// value.Type is a *TypeReference, so we need to access the Type field
	isStruct := false
	if value.Type.Type != nil && value.Type.Type.Kind == cucarachareflex.TypeKindStruct {
		isStruct = true
	}

	if isStruct {
		// Output struct literal syntax: TypeName{field1: value1, field2: value2}
		fmt.Fprintf(g.w, "%s{\n", g.sanitizeTypeName(value.Type))

		nextIndent := indent + 1
		indentStr := strings.Repeat("\t", nextIndent)

		// Sort keys for deterministic output
		keys := make([]*cucarachareflex.Value, 0, len(mapValues))
		for key := range mapValues {
			keys = append(keys, key)
		}
		slices.SortFunc(keys, func(a, b *cucarachareflex.Value) int {
			return a.Compare(b)
		})

		// Iterate over sorted keys (field names)
		for _, key := range keys {
			val := mapValues[key]
			// Key should be a *Value wrapping a string (field name)
			fieldName := "<unknown>"
			if keyStr, ok := key.Value.(string); ok {
				fieldName = keyStr
			}
			fmt.Fprintf(g.w, "%s%s: ", indentStr, fieldName)
			err := g.generateValueInternal(val, nextIndent, ",\n")
			if err != nil {
				return fmt.Errorf("failed to generate value for struct field %s: %w", fieldName, err)
			}
		}

		fmt.Fprintf(g.w, "%s}%s", strings.Repeat("\t", indent), outputEnd)
		return nil
	}

	// Regular map - output: map[KeyType]ValueType{key: value, ...}
	fmt.Fprintf(g.w, "map[%s]%s{\n", g.sanitizeTypeName(value.Type.Type.Key), g.sanitizeTypeName(value.Type.Type.Value))

	nextIndent := indent + 1
	indentStr := strings.Repeat("\t", nextIndent)

	// Sort the keys for deterministic output
	keys := make([]*cucarachareflex.Value, 0, len(mapValues))
	for key := range mapValues {
		keys = append(keys, key)
	}
	slices.SortFunc(keys, func(a, b *cucarachareflex.Value) int {
		return a.Compare(b)
	})

	// Check if the key type is an enum - if so, we'll use constant names instead of raw values
	keyTypeName := g.sanitizeTypeName(value.Type.Type.Key)
	var enumDef *cucarachareflex.Enum
	if g.pkg != nil && g.pkg.Enums != nil {
		enumDef = g.pkg.Enums[keyTypeName]
	}

	// Iterate over sorted keys
	for _, key := range keys {
		val := mapValues[key]
		fmt.Fprintf(g.w, "%s", indentStr)

		// If the key type is an enum, look up the constant name
		if enumDef != nil {
			constantName := ""
			for _, constant := range enumDef.Values {
				if constant.Value.Compare(key) == 0 {
					constantName = constant.Name
					break
				}
			}
			if constantName != "" {
				io.WriteString(g.w, constantName)
			} else {
				// Fallback to raw value if constant not found
				err := g.generateValueInternal(key, nextIndent, "")
				if err != nil {
					return fmt.Errorf("failed to generate value for map key: %w", err)
				}
			}
		} else {
			err := g.generateValueInternal(key, nextIndent, "")
			if err != nil {
				return fmt.Errorf("failed to generate value for map key: %w", err)
			}
		}

		io.WriteString(g.w, ": ")
		err := g.generateValueInternal(val, nextIndent, ",\n")
		if err != nil {
			return fmt.Errorf("failed to generate value for map value: %w", err)
		}
	}

	fmt.Fprintf(g.w, "%s}%s", strings.Repeat("\t", indent), outputEnd)
	return nil
}

func (g *Generator) generatePointerValue(value *cucarachareflex.Value, indent int, outputEnd string) error {
	// We expect the value to be a pointer to another value
	elemValue, ok := value.Value.(*cucarachareflex.Value)
	if !ok {
		return fmt.Errorf("expected pointer value to be a pointer to another value, got %T", value.Value)
	}

	fmt.Fprintf(g.w, "&")
	return g.generateValueInternal(elemValue, indent, outputEnd)
}

func (g *Generator) generateInterfaceValue(value *cucarachareflex.Value, indent int, outputEnd string) error {
	// For interface values, we just generate the underlying value
	underlyingValue, ok := value.Value.(*cucarachareflex.Value)
	if !ok {
		return fmt.Errorf("expected interface value to be a pointer to another value, got %T", value.Value)
	}

	return g.generateValueInternal(underlyingValue, indent, outputEnd)
}

func (g *Generator) sanitizeTypeName(t *cucarachareflex.TypeReference) string {
	// If the type name is a built-in type, we can return it as-is
	if basicType := cucarachareflex.GetBasicType(t.Name); basicType != nil {
		return t.Name
	}

	// If the type belongs to the output package, name with unqualified namee.g. "MyType"
	if g.outputPackage != "" && strings.HasPrefix(t.Name, g.outputPackage) {
		return strings.TrimPrefix(t.Name, g.outputPackage+".")
	}

	// Otherwise, return the fully qualified name
	return t.Name
}

func (g *Generator) generateAliasValue(value *cucarachareflex.Value, indent int, outputEnd string) error {
	// For alias/typedef values, we need to output the typedef name cast around the underlying value.
	// The key insight: use value.Type metadata to determine what to generate, not the raw value.

	fmt.Fprintf(g.w, "%s(", g.sanitizeTypeName(value.Type))

	// If the value is already wrapped as a *Value, just recurse
	underlyingValue, ok := value.Value.(*cucarachareflex.Value)
	if ok {
		// Already wrapped - just generate it
		err := g.generateValueInternal(underlyingValue, indent, "")
		if err != nil {
			return fmt.Errorf("failed to generate value for alias: %w", err)
		}
	} else {
		// Raw value - we need to use the underlying type from the typedef metadata
		// to generate properly. Create a temporary Value with the correct type.
		if value.Type.Type == nil || value.Type.Type.OriginalType == nil {
			return fmt.Errorf("typedef %s has no OriginalType metadata", value.Type.Name)
		}

		// Create a synthetic *Value with the underlying type and the raw value
		syntheticValue := &cucarachareflex.Value{
			Type:  value.Type.Type.OriginalType,
			Value: value.Value,
		}
		err := g.generateValueInternal(syntheticValue, indent, "")
		if err != nil {
			return fmt.Errorf("failed to generate value for typedef %s: %w", value.Type.Name, err)
		}
	}

	io.WriteString(g.w, ")"+outputEnd)
	return nil
}

// Generate generates code for multiple cucaracha/reflect values in the order provided.
// It handles Function, Type (struct/interface/alias), and Enum types, calling the appropriate
// generation method for each value. Values are separated by blank lines in the output.
//
// Supported types:
//   - *cucarachareflex.Function: generates function signature
//   - *cucarachareflex.Type: generates struct, interface, or alias code based on Kind
//   - *cucarachareflex.Enum: generates enum type and String() method
//
// Returns an error if:
//   - An unsupported type is provided
//   - Code generation for a specific value fails
//   - Writing to the output fails
func (g *Generator) Generate(values ...interface{}) error {
	for i, value := range values {
		// Add blank line separator between items (but not before the first item)
		if i > 0 {
			io.WriteString(g.w, "\n")
		}

		switch v := value.(type) {
		case *cucarachareflex.Type:
			switch v.Kind {
			case cucarachareflex.TypeKindStruct:
				if err := g.GenerateStructCode(v); err != nil {
					return err
				}
			case cucarachareflex.TypeKindInterface:
				if err := g.GenerateInterfaceCode(v); err != nil {
					return err
				}
			case cucarachareflex.TypeKindAlias:
				if err := g.GenerateAliasCode(v); err != nil {
					return err
				}
			case cucarachareflex.TypeKindTypedef:
				if err := g.GenerateTypedefCode(v); err != nil {
					return err
				}
			default:
				return fmt.Errorf("unsupported type kind for generation: %s", v.Kind)
			}

		case *cucarachareflex.Enum:
			if err := g.GenerateEnumCode(v); err != nil {
				return err
			}
		case *cucarachareflex.Variable:
			if err := g.GenerateVariable(v); err != nil {
				return err
			}
		case *FunctionImplementation:
			if err := g.GenerateFunctionImplementation(v); err != nil {
				return err
			}
		case *MethodImplementation:
			if err := g.GenerateMethodImplementation(v); err != nil {
				return err
			}
		default:
			return fmt.Errorf("unsupported value type for generation: %T", value)
		}
	}

	return nil
}

// GenerateMultipleFromSlice generates code for multiple cucaracha/reflect values provided as a slice.
// This is a convenience variant of GenerateMultiple that accepts a slice instead of variadic arguments.
//
// See GenerateMultiple for details on supported types and error handling.
func (g *Generator) GenerateMultipleFromSlice(values []interface{}) error {
	return g.Generate(values...)
}

// formatParameters formats a slice of parameters into a comma-separated list
func formatParameters(params []*cucarachareflex.Parameter) string {
	if len(params) == 0 {
		return ""
	}

	var parts []string
	for _, param := range params {
		if param.Name != "" {
			parts = append(parts, fmt.Sprintf("%s %s", param.Name, param.Type.Name))
		} else {
			parts = append(parts, param.Type.Name)
		}
	}

	return strings.Join(parts, ", ")
}

// formatParametersWithParens formats parameters with parentheses when there are multiple values
func formatParametersWithParens(params []*cucarachareflex.Parameter) string {
	if len(params) == 0 {
		return ""
	}

	formatted := formatParameters(params)
	if len(params) > 1 {
		return "(" + formatted + ")"
	}
	return formatted
}

// toCommandName converts a struct/method name to command name (camelCase)
// e.g., "Step" -> "step", "RemoveBreakpoint" -> "removeBreakpoint"
func toCommandName(s string) string {
	if len(s) == 0 {
		return s
	}
	return strings.ToLower(s[0:1]) + s[1:]
}

// File represents a single Go source file to be generated.
// It contains all the types, functions, methods, variables, and enums that should be
// written to the output file. File provides a builder pattern interface for constructing
// the file definition.
type File struct {
	// Package is the Go package name for this file (e.g., "debugger", "reflect").
	// This determines the package declaration at the top of the generated file.
	Package string `json:"package"`

	// Doc is an optional file-level documentation comment.
	// If present, it will be written at the beginning of the generated file.
	Doc string `json:"doc,omitempty"`

	// Imports is a list of import paths to include in this file.
	// Examples: "fmt", "encoding/json", "github.com/user/repo/pkg"
	Imports []string `json:"imports,omitempty"`

	// Types is a list of type definitions to generate (structs, interfaces, aliases).
	Types []*cucarachareflex.Type `json:"types,omitempty"`

	// Functions is a list of function implementations to generate.
	// Each includes the function signature and a template for the body.
	Functions []*FunctionImplementation `json:"functions,omitempty"`

	// Methods is a list of method implementations to generate.
	// Each includes the method signature and a template for the body.
	Methods []*MethodImplementation `json:"methods,omitempty"`

	// Variables is a list of global variables to generate.
	Variables []*cucarachareflex.Variable `json:"variables,omitempty"`

	// Enums is a list of enum type definitions to generate.
	// Usually includes additional helper methods like String(), UnmarshalJSON(), etc.
	Enums []*cucarachareflex.Enum `json:"enums,omitempty"`
}

func NewFile(pkgName string) *File {
	return &File{
		Package: pkgName,
	}
}

func (f *File) AddImport(importPath string) *File {
	f.Imports = append(f.Imports, importPath)
	return f
}

func (f *File) AddType(typ *cucarachareflex.Type) *File {
	f.Types = append(f.Types, typ)
	return f
}

func (f *File) AddFunction(fn *FunctionImplementation) *File {
	f.Functions = append(f.Functions, fn)
	return f
}

func (f *File) AddMethod(method *MethodImplementation) *File {
	f.Methods = append(f.Methods, method)
	return f
}

func (f *File) AddVariable(variable *cucarachareflex.Variable) *File {
	f.Variables = append(f.Variables, variable)
	return f
}

func (f *File) AddEnum(enum *cucarachareflex.Enum) *File {
	f.Enums = append(f.Enums, enum)
	return f
}

// GenerateFile generates code for a single file in a package.
func (g *Generator) GenerateFile(file *File) error {
	// Write file-level code generation comment
	io.WriteString(g.w, "// Code generated by codegen; DO NOT EDIT.\n\n")

	// Write file-level documentation if available
	if file.Doc != "" {
		for _, line := range strings.Split(strings.TrimSpace(file.Doc), "\n") {
			fmt.Fprintf(g.w, "// %s\n", strings.TrimPrefix(strings.TrimSpace(line), "// "))
		}
		io.WriteString(g.w, "\n")
	}

	// Mark output package so that generated code does not include imports for the package itself
	// or that types defined in the same package are not prefixed with the package name.
	g.outputPackage = file.Package
	defer func() { g.outputPackage = "" }() // Reset after generation

	// Write package declaration
	fmt.Fprintf(g.w, "package %s\n\n", file.Package)

	if len(file.Enums) > 0 {
		file.AddImport("fmt")           // For error formatting in FromString function
		file.AddImport("encoding/json") // For JSON marshal/unmarshal methods
	}

	uniqueImports := utils.Set(file.Imports)

	if len(uniqueImports) > 0 {
		if len(uniqueImports) == 1 {
			for _, imp := range uniqueImports {
				fmt.Fprintf(g.w, "import %q\n\n", imp)
			}
		} else {
			io.WriteString(g.w, "import (\n")
			for _, imp := range uniqueImports {
				fmt.Fprintf(g.w, "\t%q\n", imp)
			}
			io.WriteString(g.w, ")\n\n")
		}
	}

	for _, typ := range file.Types {
		if err := g.Generate(typ); err != nil {
			return fmt.Errorf("failed to generate type '%s': %w", typ.Name, err)
		}
	}

	for _, fn := range file.Functions {
		if err := g.Generate(fn); err != nil {
			return fmt.Errorf("failed to generate function '%s' implementation: %w", fn.Function.Name, err)
		}
	}

	for _, method := range file.Methods {
		if err := g.Generate(method); err != nil {
			return fmt.Errorf("failed to generate method '%s' implementation: %w", method.Method.Name, err)
		}
	}

	for _, variable := range file.Variables {
		if err := g.Generate(variable); err != nil {
			return fmt.Errorf("failed to generate variable '%s': %w", variable.Name, err)
		}
	}

	for _, enum := range file.Enums {
		if err := g.Generate(enum); err != nil {
			return fmt.Errorf("failed to generate enum '%s': %w", enum.Type.Name, err)
		}
	}

	return nil
}

// Generate generates a Go source file at the specified output path based on the provided File metadata.
func Generate(file *File, outputPath string) error {
	tempFile := outputPath + ".tmp"

	// Create temporary output file (So that generation errors don't break existing file)
	f, err := os.Create(tempFile)
	if err != nil {
		return fmt.Errorf("failed to create output file: %w", err)
	}

	// Create a package from the file's enums so the generator can look them up
	pkg := &cucarachareflex.Package{
		Enums: make(map[string]*cucarachareflex.Enum),
	}
	for _, enum := range file.Enums {
		pkg.Enums[enum.Type.Name] = enum
	}

	// Create a new generator and generate the file content
	generator := NewGenerator(pkg, f)
	if err := generator.GenerateFile(file); err != nil {
		f.Close()
		os.Remove(tempFile)
		return fmt.Errorf("failed to generate file content: %w", err)
	}

	f.Close() // Close before formatting

	// Make sure the file is properly formatted
	if err := formatGoFile(tempFile); err != nil {
		// Save the unformatted output to a .badcodegen file for debugging
		badCodegenPath := outputPath + ".badcodegen"
		if renameErr := os.Rename(tempFile, badCodegenPath); renameErr == nil {
			return fmt.Errorf("failed to format generated file: %w\nUnformatted code saved to: %s", err, badCodegenPath)
		}
		// If rename fails, try reading and reporting
		unformattedContent, _ := os.ReadFile(tempFile)
		os.Remove(tempFile)
		return fmt.Errorf("failed to format generated file: %w\nUnformatted content:\n%s", err, string(unformattedContent))
	}

	// Rename the temporary file to the final output path
	if err := os.Rename(tempFile, outputPath); err != nil {
		os.Remove(tempFile)
		return fmt.Errorf("failed to rename temporary file: %w", err)
	}

	return nil
}

func formatGoFile(filePath string) error {
	cmd := exec.Command("gofmt", "-e", "-w", filePath)
	var stderr bytes.Buffer
	cmd.Stderr = &stderr

	if err := cmd.Run(); err != nil {
		return fmt.Errorf("gofmt failed: %w (stderr: %s)", err, stderr.String())
	}

	return nil
}
